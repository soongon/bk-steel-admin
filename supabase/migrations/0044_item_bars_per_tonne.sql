-- ============================================================
-- 0044_item_bars_per_tonne.sql
-- 명목 1톤 출하 표준본수 (철근, 규격×길이별) — 톤 거래 환산 기준데이터
-- 출처: docs/reference-data/rebar/rebar_weight_table_{D10_D25,D29_D41}.jpg
--        (이형철근 1톤 출하 환산표, KS D 3504)
--
-- 핵심: '1톤'은 명목 호칭이고 실제 이론중량은 규격·길이마다 924~1,123kg로 다 다름.
--   톤 거래 환산:  실제 kg = 톤수 × bars_per_tonne × weight_per_unit_kg(1본중량)
--   예) D10 8m '1톤' = 210본 × 4.48kg = 940.8kg (1000kg 아님)
--   비표준 길이(표에 없는 7m 등)는 bars_per_tonne NULL → 폼이 명목 1000kg fallback.
-- ============================================================

ALTER TABLE item ADD COLUMN IF NOT EXISTS bars_per_tonne INT;
COMMENT ON COLUMN item.bars_per_tonne IS
  '명목 1톤 출하 표준 본수(철근, 규격×길이별). 톤 환산: 톤 × bars_per_tonne × weight_per_unit_kg = 실제 이론중량 kg. 출처: 이형철근 1톤 출하 환산표.';

UPDATE item AS i
SET bars_per_tonne = v.bars
FROM (VALUES
  ('D10', 6, 300), ('D10', 8, 210), ('D10', 9, 210), ('D10', 10, 180), ('D10', 11, 150), ('D10', 12, 150),
  ('D13', 6, 160), ('D13', 8, 120), ('D13', 9, 120), ('D13', 10, 100), ('D13', 11, 100), ('D13', 12,  80),
  ('D16', 6, 105), ('D16', 8,  75), ('D16', 9,  75), ('D16', 10,  60), ('D16', 11,  60), ('D16', 12,  60),
  ('D19', 6,  74), ('D19', 8,  56), ('D19', 9,  49), ('D19', 10,  44), ('D19', 11,  40), ('D19', 12,  37),
  ('D22', 6,  55), ('D22', 8,  41), ('D22', 9,  37), ('D22', 10,  33), ('D22', 11,  30), ('D22', 12,  27),
  ('D25', 6,  42), ('D25', 8,  32), ('D25', 9,  28), ('D25', 10,  25), ('D25', 11,  23), ('D25', 12,  21),
  ('D29', 6,  33), ('D29', 8,  25), ('D29', 9,  22), ('D29', 10,  20), ('D29', 11,  18), ('D29', 12,  17),
  ('D32', 6,  27), ('D32', 8,  20), ('D32', 9,  18), ('D32', 10,  16), ('D32', 11,  15), ('D32', 12,  13),
  ('D35', 6,  22), ('D35', 8,  17), ('D35', 9,  15), ('D35', 10,  13), ('D35', 11,  12), ('D35', 12,  11),
  ('D38', 6,  19), ('D38', 8,  14), ('D38', 9,  12), ('D38', 10,  11), ('D38', 11,  10), ('D38', 12,   9),
  ('D41', 6,  16), ('D41', 8,  12), ('D41', 9,  11), ('D41', 10,  10), ('D41', 11,   9), ('D41', 12,   8)
) AS v(spec, len, bars)
WHERE i.category = 'rebar'
  AND i.rebar_spec_code = v.spec
  AND i.length_m = v.len
  AND i.deleted_at IS NULL;
